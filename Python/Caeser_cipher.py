# 2 list helps to solve the index out of range error.
def caeser(plain_text, shift_no, cipher_direction):
    ciphered_text=""
    if cipher_direction == "decode":
            shift_no *= -1
    for char in plain_text:
        if char in alphabet:
            position = alphabet.index(char)
            new_position = position + shift_no
            ciphered_text += alphabet[new_position]
        else:
            ciphered_text += char         
    print(f"The {cipher_direction}d text is {ciphered_text}")
    
